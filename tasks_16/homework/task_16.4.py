"""
F(n) = 2, при n <= 1
F(n) = F(n-1) + F(n-2) + 2*n + 4, если n > 1
Чему равно значение функции F(25)?
"""


def F(n):
    if n > 1:
        return F(n-1) + F(n-2) + 2 * n + 4
    else:
        return 2


print(F(25))  # 1606706
# Не совпало с Excel'ем - 1485313
# Теперь совпало. F(1) = 2, а не 1. 1606706
