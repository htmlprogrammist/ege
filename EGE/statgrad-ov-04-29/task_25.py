"""
–ù–∞–π–¥–∏—Ç–µ –≤—Å–µ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã–µ —á–∏—Å–ª–∞, ùëÅ, –ø—Ä–∏–Ω–∞–¥–ª–µ–∂–∞—â–∏–µ –æ—Ç—Ä–µ–∑–∫—É [400 000 000; 600 000 000],
–∫–æ—Ç–æ—Ä—ã–µ –º–æ–∂–Ω–æ –ø—Ä–µ–¥—Å—Ç–∞–≤–∏—Ç—å –≤ –≤–∏–¥–µ ùëÅ = 2**m * 3**n, –≥–¥–µ ùëö ‚Äì —á—ë—Ç–Ω–æ–µ —á–∏—Å–ª–æ, ùëõ ‚Äì –Ω–µ—á—ë—Ç–Ω–æ–µ —á–∏—Å–ª–æ.
–í –æ—Ç–≤–µ—Ç–µ –∑–∞–ø–∏—à–∏—Ç–µ –≤—Å–µ –Ω–∞–π–¥–µ–Ω–Ω—ã–µ —á–∏—Å–ª–∞ –≤ –ø–æ—Ä—è–¥–∫–µ –≤–æ–∑—Ä–∞—Å—Ç–∞–Ω–∏—è.
"""
# import math

for n in range(400000000, 600000000 + 1):
    counter_even = 0
    counter_odd = 0
    number = n
    while number % 2 == 0:
        number //= 2  # –æ—Ç–±—Ä–∞—Å—ã–≤–∞—é –≤—Å–µ —Å—Ç–µ–ø–µ–Ω–∏ –¥–≤–æ–π–∫–∏
        counter_even += 1
    # math.log(number, 3)
    # —Ç–∞–º –µ—Å—Ç—å —á–∏—Å–ª–æ-–Ω—é–∞–Ω—Å, –∫–æ—Ç–æ—Ä–æ–µ –ø—Ä–∏ –ª–æ–≥–∞—Ä–∏—Ñ–º–µ –≤—ã–≤–æ–¥–∏—Ç 12.9999999999998, —Ö–æ—Ç—è –ø–æ–¥—Ö–æ–¥–∏—Ç (3 ** 13)
    # https://colab.research.google.com/drive/1ZuBfkGIYGs6CzR7LG9gnwy8eBR3gL4XR?authuser=2#scrollTo=5vjZqcu57RiL&line=1&uniqifier=1
    while number % 3 == 0:
        number //= 3  # –æ—Ç–±—Ä–∞—Å—ã–≤–∞—é –≤—Å–µ —Å—Ç–µ–ø–µ–Ω–∏ –¥–≤–æ–π–∫–∏
        counter_odd += 1
    if counter_odd % 2 != 0 and counter_even % 2 == 0 and counter_even != 0:
        if 2 ** counter_even * 3 ** counter_odd == n:
            print(n)
